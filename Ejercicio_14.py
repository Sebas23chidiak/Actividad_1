# Este programa solicita al usuario que ingrese un número y luego calcula su cuadrado y su cubo.
# Solicitamos al usuario ingresar un número.
nu = float(input("Ingrese un número: "))
# Calculamos el cuadrado del número ingresado elevándolo al cuadrado.
r_cuadrado = nu ** 2
# Calculamos el cubo del número ingresado elevándolo al cubo.
r_cubo = nu ** 3
# Mostramos los resultados.
print("El cuadrado de", nu, "es", r_cuadrado)  # Mostramos el resultado del cuadrado del número.
print("El cubo de", nu, "es", r_cubo)  # Mostramos el resultado del cubo del número.
